#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    //定义整型变量a, b, c
    int a = 3;
    int b = 5;
    int c;
    
    c = a + b;
    
    printf("%d\n",  c);//把c的值输出
    printf("c = %d\n",  c);//把c的值输出
    printf("%d + %d  = %d\n", a, b, c);//把c的值输出

    return 0;//成功完成

}
